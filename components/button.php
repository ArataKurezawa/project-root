<?php
// components/button.php
// Компонент "Кнопка" для UI-блока

/*
    Варианты кнопок:
    - Обычная активная кнопка с градиентом (фиолетовый-синий)
    - Кнопка при наведении (градиент меняет направление)
    - Кнопка при нажатии (градиент фиксируется)
    - Неактивная кнопка (серая, disabled)
*/

/* 
    Для стилизации кнопки используются классы:
    - .ui-button — базовый стиль кнопки
    - .ui-button--disabled — неактивная кнопка
    - .ui-button--loading — состояние загрузки (если нужно)
    - .ui-button--wide — широкая кнопка (если нужно)
    Состояния hover, active реализуются через CSS
*/
?>

<!-- Активная кнопка (по умолчанию) -->
<button class="ui-button" type="button">
    <!-- Текст кнопки -->
    Отправить
</button>

<!-- Кнопка при наведении (стилизация через CSS, пример для теста) -->
<button class="ui-button ui-button--hover" type="button">
    Навести
</button>

<!-- Кнопка при нажатии (стилизация через CSS, пример для теста) -->
<button class="ui-button ui-button--active" type="button">
    Нажата
</button>

<!-- Неактивная кнопка (disabled) -->
<button class="ui-button ui-button--disabled" type="button" disabled>
    Неактивна
</button>

<!-- 
    Комментарии по структуре:
    - .ui-button — основной класс кнопки, отвечает за градиент, скругления, тени, анимации
    - .ui-button--hover — дополнительный класс для имитации наведения (для теста, в реальности hover через CSS)
    - .ui-button--active — дополнительный класс для имитации нажатия (для теста, в реальности active через CSS)
    - .ui-button--disabled — неактивная кнопка (серый цвет, курсор not-allowed, отключена)
    - type="button" — тип кнопки (можно заменить на submit по необходимости)
    - Текст внутри кнопки меняется по задаче

    Для работы кнопки потребуется стилизация в _ui.scss и логика (если нужно) в Button.js.
-->
